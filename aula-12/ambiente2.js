// Inicializa a variável 'c' com o valor 1
var c = 1;

// Inicia um loop 'do-while'
do {
  // Imprime uma mensagem indicando o número de fatias de pizza com a variável 'c'
  console.log(`Comi ${c} fatias de pizza!`);

  // Incrementa a variável 'c' em 1 para a próxima iteração
  c++;

  // O loop continua enquanto 'c' é menor ou igual a 6
} while (c <= 6);

// O loop 'do-while' garante que o bloco de código seja executado pelo menos uma vez, mesmo que a condição seja falsa desde o início
