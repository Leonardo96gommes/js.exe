// Inicializa a variável 'c' com o valor 1
var c = 1;

// Inicia um loop while com a condição 'c <= 6'
while (c <= 6) {
  // Imprime uma mensagem indicando o número de fatias de pizza com a variável 'c'
  console.log(`Comi ${c} fatias de pizza!`);

  // Incrementa a variável 'c' em 1 para a próxima iteração
  c++;
}

// O loop termina quando 'c' é maior que 6, e o controle passa para o próximo bloco de código (se houver)
